<template>
	<button
		class="button"
		@click="botaoClicado"
		:disabled="desabilitado"
	>
		<span class="icon">
			<i :class="icone"></i>
		</span>
		<span>{{ texto }}</span>
	</button>
</template>

<script lang="ts">
	import { defineComponent } from 'vue';

	export default defineComponent({
		props: {
			texto: {
				type: String,
				required: true,
			},
			icone: {
				type: String,
				required: true,
			},
			desabilitado: {
				type: Boolean,
				default: true,
			},
		},

		methods: {
			botaoClicado(): void {
				this.$emit('clicado');
			},
		},
	});
</script>
